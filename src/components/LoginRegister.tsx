import React, { useState } from "react";
import { Mail, Lock, Recycle } from "lucide-react";
import { signInWithEmailAndPassword, signInWithPopup } from "firebase/auth";
import { auth, googleProvider } from "../firebase";
import { doc, setDoc, serverTimestamp } from "firebase/firestore";
import { db } from "../firebase";
import { createUserIfNotExists } from "../services/userService";
interface LoginRegisterProps {
  onLogin: () => void;
}

export default function LoginRegister({ onLogin }: LoginRegisterProps) {
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [loading, setLoading] = useState(false);
  const [errorMsg, setErrorMsg] = useState("");

  const handleEmailLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    setErrorMsg("");

    try {
      await signInWithEmailAndPassword(auth, email, password);
      onLogin();
    } catch (error: any) {
      // Correct Firebase Auth error codes for email/password
      switch (error.code) {
        case "auth/user-not-found":
          setErrorMsg("User not found. Please check your email.");
          break;
        case "auth/wrong-password":
          setErrorMsg("Incorrect password. Try again.");
          break;
        case "auth/invalid-email":
          setErrorMsg("Invalid email format.");
          break;
        case "auth/invalid-credential":
          setErrorMsg("Invalid credentials. Please try again.");
          break;
        default:
          setErrorMsg("Login failed: " + error.message);
          break;
      }
    } finally {
      setLoading(false);
    }
  };

  const handleGoogleLogin = async () => {
    setLoading(true);
    setErrorMsg("");

    try {
      await signInWithPopup(auth, googleProvider);
      await createUserIfNotExists();
      onLogin();
    } catch (error: any) {
      if (error.code === "auth/popup-closed-by-user") {
        setErrorMsg("Login cancelled by user.");
      } else {
        setErrorMsg("Google login failed: " + error.message);
      }
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="min-h-screen bg-white flex flex-col items-center justify-center px-8">
      <div className="w-full max-w-md">
        {/* Logo */}
        <div className="flex flex-col items-center mb-8">
          <div className="w-20 h-20 bg-[#34A853] rounded-3xl flex items-center justify-center mb-4 shadow-lg">
            <Recycle className="w-12 h-12 text-white" />
          </div>
          <h1 className="text-[#34A853]">TelBin</h1>
          <p className="text-gray-500">Smart Waste Sorting Assistant</p>
        </div>

        {/* Login Form */}
        <div className="bg-white rounded-3xl p-8 shadow-lg border border-gray-100">
          <h2 className="text-center mb-6">Login</h2>

          <form onSubmit={handleEmailLogin} className="space-y-4">

            {/* Error Message */}
            {errorMsg && (
              <div className="flex items-center bg-red-100 border border-red-400 px-4 py-2 rounded transition-all text-red-600">
                <svg
                  className="w-5 h-5 mr-2 flex-shrink-0"
                  fill="none"
                  stroke="currentColor"
                  viewBox="0 0 24 24"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M12 8v4m0 4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z"
                  />
                </svg>
                <span className="text-sm text-red-600">{errorMsg}</span>
              </div>
            )}

            {/* Email Input */}
            <div className="relative">
              <Mail className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-gray-400" />
              <input
                type="email"
                placeholder="Email"
                value={email}
                onChange={(e) => {
                  setEmail(e.target.value);
                  setErrorMsg(""); // clear error when user types
                }}
                className={`w-full pl-12 pr-4 py-3 rounded-full border-2 focus:outline-none transition-colors ${
                  errorMsg ? 'border-red-400' : 'border-gray-200 focus:border-[#34A853]'
                }`}
                required
              />
            </div>

            <div className="relative">
              <Lock className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-gray-400" />
              <input
                type="password"
                placeholder="Password"
                value={password}
                onChange={(e) => {
                  setPassword(e.target.value);
                  setErrorMsg(""); // clear error when user types
                }}
                className={`w-full pl-12 pr-4 py-3 rounded-full border-2 focus:outline-none transition-colors ${
                  errorMsg ? 'border-red-400' : 'border-gray-200 focus:border-[#34A853]'
                }`}
                required
              />
            </div>

            <button
              type="submit"
              disabled={loading}
              className="w-full bg-[#34A853] text-white rounded-full py-3 hover:bg-green-600"
            >
              {loading ? "Logging in..." : "Login with Email"}
            </button>
          </form>

          {/* Divider */}
          <div className="relative my-6">
            <div className="absolute inset-0 flex items-center">
              <div className="w-full border-t border-gray-200"></div>
            </div>
            <div className="relative flex justify-center">
              <span className="px-4 bg-white text-gray-500">or</span>
            </div>
          </div>

          {/* âœ… GOOGLE LOGIN */}
          <button
            type="button"
            onClick={handleGoogleLogin}
            disabled={loading}
            className="w-full border-2 border-gray-200 rounded-full py-3 flex items-center justify-center gap-2 hover:bg-gray-50"
          >
            <svg className="w-5 h-5" viewBox="0 0 24 24">
              <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
              <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
              <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
              <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
            </svg>
            Continue with Google
          </button>
        </div>
      </div>
    </div>
  );
}
